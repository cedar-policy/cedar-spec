#![no_main]
use cedar_drt::logger::initialize_log;
use cedar_drt_inner::{
    fuzz_target,
    symcc::{TwoPolicyFuzzTargetInput, assert_that_asserts_match},
};

use cedar_lean_ffi::CedarLeanFfi;
use cedar_policy::{Policy, Schema};
use cedar_policy_symcc::{CompiledPolicy, matches_disjoint_asserts};

use log::debug;
use std::convert::TryFrom;

// Fuzz target checking that Asserts (`Term`s) generated by Lean and Rust SymCC
// implementations for MatchesDisjoint are equivalent
fuzz_target!(|input: TwoPolicyFuzzTargetInput| {
    initialize_log();
    let lean_ffi = CedarLeanFfi::new();
    let policy1: Policy = input.policy1.into();
    let policy2: Policy = input.policy2.into();
    debug!("Schema: {}\n", input.schema.schemafile_string());
    debug!("Policy1: {policy1}\n");
    debug!("Policy2: {policy2}\n");

    if let Ok(schema) = Schema::try_from(input.schema) {
        let lean_schema = lean_ffi.load_lean_schema_object(&schema).unwrap();
        for req_env in schema.request_envs() {
            // The validator DRT property we've been testing is that
            // rust_passes_validation => lean_passes_validation
            // So, we run the Rust validator first (as part of `compile()`) and
            // obtain post-typecheck, well-typed policies (available as
            // `.policy()` on the `CompiledPolicy`.) These policies should
            // be also well-typed according to Lean.
            if let Ok(cp1) = CompiledPolicy::compile(&policy1, &req_env, &schema)
                && let Ok(cp2) = CompiledPolicy::compile(&policy2, &req_env, &schema)
            {
                let rust_asserts = matches_disjoint_asserts(&cp1, &cp2);
                let lean_asserts = lean_ffi
                    .asserts_of_check_matches_disjoint(
                        &cp1.policy().clone().try_into().unwrap(),
                        &cp2.policy().clone().try_into().unwrap(),
                        lean_schema.clone(),
                        &req_env,
                    )
                    // should succeed on the post-typecheck policies that were successfully produced by `CompiledPolicy::compile()`
                    .unwrap()
                    .unwrap();
                assert_that_asserts_match(rust_asserts, lean_asserts);
            }
        }
    }
});
